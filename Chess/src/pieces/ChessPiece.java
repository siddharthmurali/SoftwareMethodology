package pieces;

public abstract class ChessPiece {
	
	public char color;
	public String type;
	public int move;
	
	public ChessPiece(char color){
		this.color = color;
		this.type = null;
		this.move = 0;	
	}
	
	public String toString(){
		return this.color + "";
	}
	
	public boolean validMove(){
		return true;
	}
	
}
